    N, C, H, W = img.size()
    loss = torch.sum(torch.pow(img[:, :, :H - 1, :] - img[:, :, 1:, :], 2))
    loss += torch.sum(torch.pow(img[:, :, :, :W - 1] - img[:, :, :, 1:], 2))
    loss *= tv_weight
    
    return loss